export async function readID(tracksID: string[]): Promise<string[]> {
  let random: string[] = [];
  try {
    while (random.length < 6) {
      let randomID: string =
        tracksID[Math.floor(Math.random() * tracksID.length)];
      if (!random.includes(randomID)) {
        random.push(randomID);
      }
    }
  } catch (error) {
    console.log("Reading error");
  }
  return random;
}
